    function validateForm(e) {
        
        var currentForm = event.target;

        e.preventDefault();

        let emailVal = document.getElementById('email').value;
        
        let emailValidationResult = validateEmail(emailVal);
        
        if(!emailValidationResult && null){
            
            //document.getElementById('userEmail').focus();
            document.getElementById('emailError').display='block';
            return false;
        }
        
        currentForm.submit();
    }


    function validateEmail(e) {
        let emailVal = document.getElementById('email').value;

        let emailRegx =  /^([\w.-]+)@(\[(\d{1,3}\.){3}|(?!hotmail|gmail|yahoo|microsoft|gmx|rediff|outlook|zoho|mailbox|t\-online|web\.|online\.|aol\.|live\.)(([a-zA-Z\d-]+\.)+))([a-zA-Z]{2,63}|\d{1,3})(\]?)$/;

        if(emailRegx.test(emailVal) == false) {
            //alert('Kindly use your personal email domain id');
            document.getElementById('emailError').style.display='block';
            return false;
        }
        document.getElementById('emailError').style.display='none';

        return true
    }
